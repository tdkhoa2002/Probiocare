// Returns all parts of the symbol
export function parseFullSymbol(fullSymbol) {
  const match = fullSymbol.match(/^(\w+):(\w+)\/(\w+)$/)
  if (!match) {
    return null
  }
  return {exchange: match[1], fromSymbol: match[2], toSymbol: match[3]}
}

export async function getLastBar(datadeedURL, params) {
  return new Promise((resolve, reject) => {
    ;(async () => {
      try {
        const query = `?symbol=${params.pair}&from=${params.from}&to=${params.from}`
        const res = await (await fetch(`${datadeedURL}/trading-data/chart/candle${query}`)).json()

        let data = []
        if (res && res.data.length !== undefined) {
          data = res.data.map((item) => ({...item, dt: Math.floor(item.timestamp / 1000)}))
          data = data.filter((o) => o.timestamp >= params.from && o.timestamp <= params.to)
        }

        resolve(data)
      } catch (err) {
        reject(err)
      }
    })()
  })
}

export function convertToOHLC(data, interval) {
  const sData = [, , ...data.sort((a, b) => a.time - b.time)]

  var result = []
  let isFirst = true
  let lastBar

  let rangeTime = sData[2].time + interval

  var tempObject = {}
  sData.forEach((bar) => {
    if (bar.time <= rangeTime) {
      if (isFirst) {
        tempObject.time = bar.time
        tempObject.open = bar.price
        tempObject.close = bar.price
        tempObject.high = bar.price
        tempObject.low = bar.price
      } else {
        const high = tempObject.high > bar.price ? tempObject.high : bar.price
        const low = tempObject.low < bar.price ? tempObject.low : bar.price

        tempObject.time = bar.time
        tempObject.open = lastBar.close
        tempObject.close = bar.price
        tempObject.high = high
        tempObject.low = low
      }
    } else if (tempObject.time) {
      lastBar = {...tempObject}
      rangeTime = tempObject.time + interval
      result.push({...lastBar})
      isFirst = false

      tempObject.time = bar.time
      tempObject.open = lastBar.close
      tempObject.close = bar.price
      tempObject.high = bar.price
      tempObject.low = bar.price
    }
  })

  return result
}

